package cn.seeu.entry;

public class Menu {
    private int id;

    private String name;

    private int isDelete;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getIsDelete() {
        return isDelete;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setIsDelete(int isDelete) {
        this.isDelete = isDelete;
    }
}
